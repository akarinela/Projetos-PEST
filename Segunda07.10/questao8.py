#Crie um programa para calcular a distância entre os pontos (x1,y1) e (x2,y2)

x1 = int(input("Digite o x1: "))
y1 = int(input("Digite o y1: "))
x2 = int(input("Digite o x2: "))
y2 = int(input("Digite o y2: "))

soma1 = x1 - y1
soma2 = x2 - y2

print(f'A distancia do primeiro pronto é: {soma1} o segundo é: {soma2}')

