# 9. Contar Comprimento
# Escreva uma função chamada comprimento_string que receba uma string como entrada e
# retorne o número de caracteres da string.
# Exemplo de funcionamento:
# 1 Entrada : comprimento_string (" Python ")
# 2 Sa í da : 6

def comprimento_string(string: str):
    return len(string)

print(comprimento_string('Python'))